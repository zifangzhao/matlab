for m=length(s):-1:1
    if s(m).Eccentricity>ecc || s(m).Eccentricity<eccl 
        s(m)=[];
    end
end
bbox=cat(1,s.BoundingBox);
figure(1);
imshow(Io);
hold on
for num=1:length(s);
rectangle('Position',[bbox(num,1),bbox(num,2),bbox(num,3),bbox(num,4)],...
    'Curvature',[0.8,0.4],'LineWidth',2,'LineStyle','--','EdgeColor','g')
end
hold off

set(handles.text6,'string',num2str(length(s)));

